    <h1>PHP Functions</h1>

    <h2>What is a Function?</h2>

    <p>
        A function is a group of individual statements grouped into a logical unit. This is equivalent
        to what some languages call a subroutine.
    </p>

    <h2>Function Calls</h2>

    <p>
        When we want to execute the code in a function, we "call" (AKA invoke) the function and
        the statement used to do that is referred to as a "function call." The function call
        includes:
    </p>

    <ul>
        <li>Function name</li>
        <li>Arguments (AKA actual parameters) inside parentheses</li>
    </ul>

    <p>
        Some functions return a value, others do not. Functions can also generate output whether
        or not they return a value.
    </p>
    
    <h2>Echo and Print</h2>

    <p>
        Echo and print are used to output information (text, HTML, or something else) to the
        client (browser). They are easily confused with functions, but are actually programming
        language constructs and not functions, which is why we don't need the parentheses.
    </p>

    <p>
        Echo and print do the same thing, but print returns the value 1 and echo returns nothing.
        Also, print accepts a single string to output while echo will accept any number of strings.
    </p>

    
